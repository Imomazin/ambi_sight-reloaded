// Helper functions
export function getToolsByCategory(category: ToolCategory): StrategyToolFull[] {
  return strategyToolsLibrary.filter(tool => tool.category === category);
}

export function getToolsByPlan(plan: Plan): StrategyToolFull[] {
  const planHierarchy: Record<Plan, Plan[]> = {
    'Free': ['Free'],
    'Starter': ['Free', 'Starter'],
    'Pro': ['Free', 'Starter', 'Pro'],
    'Enterprise': ['Free', 'Starter', 'Pro', 'Enterprise'],
  };
  const allowedPlans = planHierarchy[plan];
  return strategyToolsLibrary.filter(tool => allowedPlans.includes(tool.requiredPlan));
}

export function getToolById(id: string): StrategyToolFull | undefined {
  return strategyToolsLibrary.find(tool => tool.id === id);
}

export function searchTools(query: string): StrategyToolFull[] {
  const lowerQuery = query.toLowerCase();
  return strategyToolsLibrary.filter(tool =>
    tool.name.toLowerCase().includes(lowerQuery) ||
    tool.description.toLowerCase().includes(lowerQuery) ||
    tool.category.toLowerCase().includes(lowerQuery) ||
    tool.bestFor.some(role => role.toLowerCase().includes(lowerQuery))
  );
}

export function getRelatedTools(toolId: string): StrategyToolFull[] {
  const tool = getToolById(toolId);
  if (!tool) return [];
  return tool.relatedTools
    .map(id => getToolById(id))
    .filter((t): t is StrategyToolFull => t !== undefined);
}

